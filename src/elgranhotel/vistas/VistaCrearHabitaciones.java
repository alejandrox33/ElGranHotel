    private void bBuscar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscar2ActionPerformed
        if(rbLibre.isSelected() == false && rbOcupadas.isSelected() == false){
          JOptionPane.showMessageDialog(null, "Seleccione una opcion de busqueda");
          
        }else if (rbLibre.isSelected() || rbOcupadas.isSelected()) {
           
            borrarFilasTablas();
            estado = rbLibre.isSelected();
            listaHabitaciones = habD.obtenerHabiporTipo(!estado);
            
            for (Habitacion hab : listaHabitaciones) {
                modelo.addRow(new Object[]{hab.getId_habitacion(), hab.getEstado(), hab.getThabitacion().getId_thabitacion(),
                    hab.getThabitacion().getTipo(), hab.getThabitacion().getCodigo(), hab.getThabitacion().getCantPersonas(),
                    hab.getThabitacion().getCantCamas(), hab.getThabitacion().getTipoCamas(), hab.getThabitacion().getPrecioNoche()});
            }
            
            simple = habD.contadorHabitacionPorTipo("Simple");
            doble = habD.contadorHabitacionPorTipo("Doble");
            triple = habD.contadorHabitacionPorTipo("Triple");
            suitL = habD.contadorHabitacionPorTipo("Suit Lujo");

            lSimple.setText(String.valueOf(simple));
            lDoble.setText(String.valueOf(doble));
            lTriple.setText(String.valueOf(triple));
            lSL.setText(String.valueOf(suitL));
        }
        
    }//GEN-LAST:event_bBuscar2ActionPerformed

