    private void bCrearHabitacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCrearHabitacionActionPerformed
        if (tfDesde.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un numero de Habitacion Desde");

        } else if (tfHasta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un numero de Habitacion Hasta");

        } else if (tfTipo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un Tipo");

        } else if (tfCodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un Codigo");

        } else if (tfCantPersonas.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese una Cantidad de Personas");

        } else if (tfTipoCamas.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un Tipo de Camas");

        } else if (tfCantCamas.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese una Cantidad de Camas");

        } else if (tfPrecioNoche.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un Precio por Noche");

        } else {
            int desde = Integer.parseInt(tfDesde.getText());
            int hasta = Integer.parseInt(tfHasta.getText());
            String tipo = tfTipo.getText();
            int codigo = Integer.parseInt(tfCodigo.getText());
            int cantP = Integer.parseInt(tfCantPersonas.getText());
            String tipoC = tfTipoCamas.getText();
            int cantC = Integer.parseInt(tfCantCamas.getText());
            double precioN = Double.parseDouble(tfPrecioNoche.getText());

            habD.crearHabitacionPorTipo(desde, hasta, tipo, codigo, cantP, cantC, tipoC, precioN, false);

            JOptionPane.showMessageDialog(null, "Las Habitaciones se crearon con Exito");

            tfDesde.setText("");
            tfHasta.setText("");
            tfTipo.setText("");
            tfCodigo.setText("");
            tfCantPersonas.setText("");
            tfTipoCamas.setText("");
            tfCantCamas.setText("");
            tfPrecioNoche.setText("");
        }


    }//GEN-LAST:event_bCrearHabitacionActionPerformed

